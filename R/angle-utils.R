#' Degree to radian conversion factor
#' @return pi / 180
deg_to_rad <- function() {
  pi / 180
}

#' Radian to degree conversion factor
#' @return 180 / pi
rad_to_deg <- function() {
  180 / pi
}

#' 90 degrees in radians (pi/2)
#' @return pi / 2
rad_90 <- function() {
  pi / 2
}

#' 450 degrees in radians (2.5 * pi)
#' @return 2 * pi * 450 / 360
rad_450 <- function() {
  2 * pi * 450 / 360
}

#' 2*pi (full circle in radians)
#' @return 2 * pi
two_pi <- function() {
  2 * pi
}
